import { MetadataRoute } from 'next'

const BASE_URL = process.env.NEXT_PUBLIC_SITE_URL || 'https://delas-sochi.ru'

export default function robots(): MetadataRoute.Robots {
  return {
    rules: {
      userAgent: '*',
      allow: '/',
      disallow: [
        '/api/',
        '/booking/',
        '/booking/success/',
        '/privacy/',
        '/terms/',
      ],
    },
    sitemap: `${BASE_URL}/sitemap.xml`,
  }
}



